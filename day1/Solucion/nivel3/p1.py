"""
Write an example for different Python data types such as 
Number(Integer, Float, Complex), String, Boolean, List, Tuple, Set and Dictionary.
"""

# Checking data types
print("Python cuenta con los siguientes tipos de datos:\n")
print(type(10), " Ej: 10")          # Int
print(type(3.14), " Ej: 3.14")        # Float
print(type(1 + 3j), " Ej: 1 + 3j")      # Complex number
print(type('Asabeneh'), " Ej: 'Asabeneh'")  # String
print(type([1, 2, 3]), " Ej: '[1, 2, 3]'")   # List
print(type({'name':'Asabeneh'})," Ej: {'name':'Asabeneh'}") # Dictionary
print(type({9.8, 3.14, 2.7}), " Ej: {9.8, 3.14, 2.7} ")    # Set
print(type((9.8, 3.14, 2.7)) , " Ej: (9.8, 3.14, 2.7)")    # Tuple
print(type(True), " Ej: True")    #Bool